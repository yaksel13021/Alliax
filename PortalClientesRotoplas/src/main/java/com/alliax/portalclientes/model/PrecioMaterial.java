package com.alliax.portalclientes.model;

import java.math.BigDecimal;

public class PrecioMaterial {
    private BigDecimal monto;
    private BigDecimal precioNeto;
    private String iva;
    private BigDecimal totalPartida;
    private String moneda;
    private String fechaEntrega;
    private String mensajeError;
    private String codigoError;

    public BigDecimal getMonto() {
        return monto;
    }

    public void setMonto(BigDecimal monto) {
        this.monto = monto;
    }

    public BigDecimal getPrecioNeto() {
        return precioNeto;
    }

    public void setPrecioNeto(BigDecimal precioNeto) {
        this.precioNeto = precioNeto;
    }

    public String getIva() {
        return iva;
    }

    public void setIva(String iva) {
        this.iva = iva;
    }

    public BigDecimal getTotalPartida() {
        return totalPartida;
    }

    public void setTotalPartida(BigDecimal totalPartida) {
        this.totalPartida = totalPartida;
    }

    public String getMoneda() {
        return moneda;
    }

    public void setMoneda(String moneda) {
        this.moneda = moneda;
    }

    public String getFechaEntrega() {
        return fechaEntrega;
    }

    public void setFechaEntrega(String fechaEntrega) {
        this.fechaEntrega = fechaEntrega;
    }

    public String getMensajeError() {
        return mensajeError;
    }

    public void setMensajeError(String mensajeError) {
        this.mensajeError = mensajeError;
    }

    public String getCodigoError() {
        return codigoError;
    }

    public void setCodigoError(String codigoError) {
        this.codigoError = codigoError;
    }

    @Override
    public String toString() {
        return "PrecioMaterial{" +
                "monto=" + monto +
                ", precioNeto=" + precioNeto +
                ", iva='" + iva + '\'' +
                ", totalPartida=" + totalPartida +
                ", moneda='" + moneda + '\'' +
                ", fechaEntrega='" + fechaEntrega + '\'' +
                ", mensajeError='" + mensajeError + '\'' +
                ", codigoError='" + codigoError + '\'' +
                '}';
    }
}
